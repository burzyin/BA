package com.exadel.belarusattractions.dto.routes;

import com.exadel.belarusattractions.common.AbstractUIObject;

import java.util.List;

/**
 * The model of route.
 * </p>
 * Developer: Paulau Aliaksandr
 * Created: 5:34 PM, 12/29/12
 */

public class Route extends AbstractUIObject {
    private Long id;
    private String name;
    private String title;
    private String description;
    private List<Long> wayPoints;
    private String cost;

    public Route() {
    }

    public Route(Long id, String name, String title, String description, List<Long> wayPoints, String cost) {
        this.id = id;
        this.name = name;
        this.title = title;
        this.description = description;
        this.wayPoints = wayPoints;
        this.cost = cost;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<Long> getWayPoints() {
        return wayPoints;
    }

    public void setWayPoints(List<Long> wayPoints) {
        this.wayPoints = wayPoints;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }
}
